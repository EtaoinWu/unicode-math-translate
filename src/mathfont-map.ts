export function translateFont(c: string, fontface: mathFontface): string {
  if (!mathFonts.hasOwnProperty(c)) {
    return c;
  }
  const font = mathFonts[c];
  if (!font.hasOwnProperty(fontface)) {
    return c;
  }
  const res = font[fontface];
  if (res === undefined) {
    return c;
  }
  return res;
}
